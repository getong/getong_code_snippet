-module(suspend).

%% API
-export([start/0]).

%%%===================================================================
%%% API
%%%===================================================================
start() ->
	spawn(fun() -> start_fun() end).


%%--------------------------------------------------------------------
%% @doc
%% @spec
%% @end
%%--------------------------------------------------------------------

%%%===================================================================
%%% Internal functions
%%%===================================================================
start_fun() ->
	receive Msg ->
			io:format("Msg:~p~n", [Msg]),
			start_fun()
	end.
